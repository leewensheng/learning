'''
is 与 == 的区别
== 判断的是内容是否相同,[2] == [2] 为true
is 判断的是 内存地址是否相同,
但是python中 [-5,257]的小整数是全局缓存的, 指向同一个内存地址,
大整数在代码块内缓存,同一代码块内的大整数内存地址相同
2 == 2是全局成立的
似乎字符串也有个常量池
'''
print('[2] == [2] is ', [2] == [2])
print(1,   2 is 2)

a = 2
print(2, a is 2)

a = str(332)
print(3, a is '332')

a = '333'
print(4, a is '333')

b = a[0,2]
print(b is '333')
